const formatDateTime = (dateString: string) => {
  const date = new Date(dateString);

  // Get day with ordinal (1st, 2nd, 3rd, etc.)
  const day = date.getDate();
  let dayWithOrdinal;
  if (day > 3 && day < 21) dayWithOrdinal = day + "th";
  else {
    switch (day % 10) {
      case 1:
        dayWithOrdinal = day + "st";
        break;
      case 2:
        dayWithOrdinal = day + "nd";
        break;
      case 3:
        dayWithOrdinal = day + "rd";
        break;
      default:
        dayWithOrdinal = day + "th";
    }
  }

  // Format time in 12-hour format
  const hours = date.getHours();
  const minutes = date.getMinutes().toString().padStart(2, "0");
  const ampm = hours >= 12 ? "PM" : "AM";
  const twelveHour = hours % 12 || 12;

  return `${dayWithOrdinal} ${date.toLocaleString("default", {
    month: "long",
  })} ${date.getFullYear()}, ${twelveHour}:${minutes} ${ampm}`;
};
